WITH PRODUCT_GROUP_YEARLY_REVENUE
AS(
    SELECT
        PRODUCT_GROUP,
        SUM(LINE_TOTAL_VALUE) AS LINE_VALUE,
        SUM(LINE_TAX_VALUE) AS LINE_TAX_VALUE,
        DATE_TRUNC('YEAR', INVOICE_DATE) AS YEARLY
    FROM
        {{ ref('transform_product_invoice') }}
    GROUP BY
        PRODUCT_GROUP,
        DATE_TRUNC('YEAR', INVOICE_DATE)
)
SELECT
    PRODUCT_GROUP,
    YEARLY,
    (LINE_VALUE-LINE_TAX_VALUE) AS TOTAL_VALUE_PRODUCT_GROUP_YEARLY
FROM
    PRODUCT_GROUP_YEARLY_REVENUE
ORDER BY
    YEARLY,
    PRODUCT_GROUP
